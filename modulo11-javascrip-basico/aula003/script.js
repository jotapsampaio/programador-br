/* Operadores aritméticos
--Adição + (soma);
--Subtração - (diminuir);
--Multiplicação * (vezes);
--Divisão / (dividido);
--Modulo % (resto da divisão);
--++ incremento (mais 1);
-- -- decremento (menos 1);
*/

//Adição +
var a = 20;
var b = 5;
var c = a + b;
console.log(c);

//Subtração -
var a = 20;
var b = 5;
var c = a - b;
console.log(c);

//Multiplicação *
var a = 20;
var b = 5;
var c = a * b;
console.log(c);

//Divisão /
var a = 20;
var b = 5;
var c = a / b;
console.log(c);

//Modulo %
var a = 20;
var b = 5;
var c = a % b;
console.log(c);

//Incremento ++
var a = 20;
var b = 5;
var c = ++a;
console.log(c);

//Decremento --
var a = 20;
var b = 5;
var c = --b;
console.log(c);
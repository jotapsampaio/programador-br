// Como declarar uma variavel?
//A ordem de declaração importa muito e o nome que elas são declaradas também. 
//Para declarar uma variavel é recomendado usar o nome em minusculo e se tiver outro nome ele deve começar com a letra maiuscula, não pode começar com um número nem pode ter espaços, simbolos, acentuação e palavras reservadas.
var a;
var b = 'João Paulo';
a = 'Sampaio';
b = a;
console.log(b);

/* Tipos de Dados 
---Boolean--- (Pode ser True(verdadeiro) e false(falso));
---Null--- (Quer dizer que é um variavel vazia);
---Underfined--- (É uma variavel indeterminada e que não sabemos o que tem nela);
---Number--- (São os tipos númericos);
---String--- (Elas ficam em parenteses e podem ser de qualquer tipo dentro delas);
*/

//Boolean
var verdadeiro = true;
console.log(verdadeiro);

//Null
var vazio = null;
console.log(vazio);

//Underfined
var indefinido;
console.log(indefinido);

//Number
var a = 10;
console.log(a);

//String
var b = 'Cadeia de caracteres \nOutra linha';
console.log(b);

//O número do tipo string é diferente do que a do tipo number já que na string ela faz concatenação e no number ele faz operações aritiméticas.